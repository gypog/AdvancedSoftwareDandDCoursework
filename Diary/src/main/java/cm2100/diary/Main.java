    private void today() {
        Calendar c = Calendar.getInstance();
        year = c.get(Calendar.YEAR);
        yearField.setText(String.valueOf(year));
        yearLabel.setText(String.valueOf(year));
        monthBox.setSelectedIndex(c.get(Calendar.MONTH));
    }
    
    private void getYear() {
        int y = validateYear(yearField.getText());
        if (y > 0) {
            year = y;
            yearLabel.setText(String.valueOf(y));
            setDays();
        }
    }
    
    public int validateYear(String f) {
        try {
            int y = Integer.parseInt(f);
            if (y > 0 && y < 10000) {
                return y;
            } else {
                return -1;
            }
        }
        catch(NumberFormatException e) {
            return -1;
        }
    }

    public void setDays() {
        javax.swing.JButton[] j = {jButton1, jButton2, jButton3, jButton4, jButton5, jButton6, jButton7, jButton8, jButton9, jButton10, jButton11, jButton12, jButton13, jButton14, jButton15, jButton16, jButton17, jButton18, jButton19, jButton20, jButton21, jButton22, jButton23, jButton24, jButton25, jButton26, jButton27, jButton28, jButton29, jButton30, jButton31, jButton32, jButton33, jButton34, jButton35, jButton36, jButton37, jButton38, jButton39, jButton40, jButton41, jButton42};
        for (int i = 0; i < j.length; i++) {
            j[i].setEnabled(false);
            j[i].setText("");
            j[i].setBackground(new JButton().getBackground());
        }
        int w = Date.dayInWeek(year, month, 1)-1;
        if (w == -1) {
            w = 6;
        }
        int x = 1;
        while (x <= Date.daysInMonth(year, month)) {
            j[w].setEnabled(true);
            j[w].setText(String.valueOf(x));
            if (diary.findAppointments(new Date(year, month, x)).isEmpty() == false) {
                j[w].setBackground(Color.red);
            }
            w ++;
            x ++;
        }
    }
    
    private void dayButton(JButton j) {
        if (j.getBackground() == Color.red) {
            s = false;
            day = Integer.parseInt(j.getText());
            output(diary.findAppointments(new Date(year, month, day)));
        } 
    }
    
    private void search() {
        search = searchField.getText();
        if (search.isBlank() == false && search.isEmpty() == false) {
            if (diary.findAppointments(search).isEmpty() == false) {
                s = true;
                output(diary.findAppointments(searchField.getText()));
            } else {
                JOptionPane.showMessageDialog(null, "No results found.");
            }
        }
    }
    
