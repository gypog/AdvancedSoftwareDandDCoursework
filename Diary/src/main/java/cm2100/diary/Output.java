    public Output(Main m, ArrayList<Appointment> app) {
        main = m;
        main.setEnabled(false);
        appointments = app;
        initComponents();
        for (Appointment a : appointments) {
            list.addElement(a.toString());
        }
        outputList.setModel(list);
        addWindowListener(new WindowAdapter() {
            @Override
            public void windowClosing(WindowEvent e)
            {
                main.setEnabled(true);
                dispose();
            }
        });
    }
    
    public void modify(Appointment a) {
        dispose();
        main.diary.delete(appointments.get(outputList.getSelectedIndex()));
        main.diary.add(a);
        main.diary.sortByDateTime();
        main.setDays();
        main.setEnabled(true);
        if (main.s) {
            if (main.diary.findAppointments(main.search).isEmpty() == false) {
                main.output(main.diary.findAppointments(main.search));
            }
        } else {
            if (main.diary.findAppointments(new Date(main.year, main.month, main.day)).isEmpty() == false) {
                main.output(main.diary.findAppointments(new Date(main.year, main.month, main.day)));
            }
        }
    }

